function add(num1, num2) {
    return num1 + num2;
}

function subtract(num1, num2) {
    return num1 - num2;
}

function multiply(num1, num2) {
    return num1 * num2;
}

function divide(num1, num2) {
    return num1/num2;
}

console.log(add(4,2));

console.log(subtract(4,8));

console.log(multiply(2,16));

console.log(divide(15,2));